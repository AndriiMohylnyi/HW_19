package DefSteps;

public class Students {
    private String name;
    private String age;
    private int course;

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public int getCourse() {
        return course;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public void setCourse(int course) {
        this.course = course;
    }
}
